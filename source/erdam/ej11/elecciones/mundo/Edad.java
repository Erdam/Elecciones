package erdam.ej11.elecciones.mundo;
/**
 * Enumeraciones para el rango de edad.
 */
public enum Edad {
	/**
	 * Representa un rango de edad de 18 a 34 años.
	 */
	JOVEN,
	/**
	 * Representa un rango de edad de 35 a 54 años
	 */
	ADULTO,
	/**
	 * Representa un rango de edad de más de 55 años en adelante.
	 */
	MAYOR

}
